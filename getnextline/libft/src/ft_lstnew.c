#include "libft.h"

t_list	*ft_lstnew(const void *content, size_t content_size)
{
	t_list *new;

	new = NULL;
	if ((new = malloc(sizeof(t_list))))
	{
		new->next = NULL;
		if (content)
		{
			new->content = malloc(content_size);
			ft_memcpy(new->content, content, content_size);
			new->content_size = content_size;
		}
		else
		{
			new->content = NULL;
			new->content_size = 0;
		}
	}
	return (new);
}
